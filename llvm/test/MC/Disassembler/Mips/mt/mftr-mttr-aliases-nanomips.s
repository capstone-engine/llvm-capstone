# RUN: llvm-mc -arch=nanomips -mattr=+mt -show-encoding < %s | FileCheck %s

# Check the various aliases of the m[ft]tr instruction.
  mftc0 $a0, $5          # CHECK: mftr  $a0, $5, 0, 0, 0         # encoding: [0x85,0x20,0x30,0x02]
  mftc0 $a2, $7, 1       # CHECK: mftr  $a2, $7, 0, 1, 0         # encoding: [0xc7,0x20,0x30,0x0a]
  mftgpr $a1, $9         # CHECK: mftr  $a1, $9, 1, 0, 0         # encoding: [0xa9,0x20,0x30,0x06]
  mftlo $t5              # CHECK: mftr  $t5, $zero, 1, 1, 0       # encoding: [0x60,0x20,0x30,0x0e]
  mftlo $t5, $ac0        # CHECK: mftr  $t5, $zero, 1, 1, 0       # encoding: [0x60,0x20,0x30,0x0e]
  mftlo $t5, $ac1        # CHECK: mftr  $t5, $4, 1, 1, 0         # encoding: [0x64,0x20,0x30,0x0e]
  mftlo $t5, $ac2        # CHECK: mftr  $t5, $8, 1, 1, 0         # encoding: [0x68,0x20,0x30,0x0e]
  mftlo $t5, $ac3        # CHECK: mftr  $t5, $12, 1, 1, 0         # encoding: [0x6c,0x20,0x30,0x0e]
  mfthi $t5, $ac0        # CHECK: mftr  $t5, $1, 1, 1, 0         # encoding: [0x61,0x20,0x30,0x0e]
  mfthi $t5, $ac1        # CHECK: mftr  $t5, $5, 1, 1, 0         # encoding: [0x65,0x20,0x30,0x0e]
  mfthi $t5, $ac2        # CHECK: mftr  $t5, $9, 1, 1, 0         # encoding: [0x69,0x20,0x30,0x0e]
  mfthi $t5, $ac3        # CHECK: mftr  $t5, $13, 1, 1, 0         # encoding: [0x6d,0x20,0x30,0x0e]
  mftacx $t5, $ac0       # CHECK: mftr  $t5, $2, 1, 1, 0         # encoding: [0x62,0x20,0x30,0x0e]
  mftacx $t5, $ac1       # CHECK: mftr  $t5, $6, 1, 1, 0         # encoding: [0x66,0x20,0x30,0x0e]
  mftacx $t5, $ac2       # CHECK: mftr  $t5, $10, 1, 1, 0         # encoding: [0x6a,0x20,0x30,0x0e]
  mftacx $t5, $ac3       # CHECK: mftr  $t5, $14, 1, 1, 0         # encoding: [0x6e,0x20,0x30,0x0e]
  mftdsp $a0             # CHECK: mftr  $a0, $16, 1, 1, 0         # encoding: [0x90,0x20,0x30,0x0e]

  mttc0 $a0, $5          # CHECK: mttr  $a0, $5, 0, 0, 0         # encoding: [0x85,0x20,0x70,0x02]
  mttc0 $a2, $7, 1       # CHECK: mttr  $a2, $7, 0, 1, 0         # encoding: [0xc7,0x20,0x70,0x0a]
  mttgpr $a1, $a5        # CHECK: mttr  $a1, $9, 1, 0, 0         # encoding: [0xa9,0x20,0x70,0x06]
  mttlo $t5              # CHECK: mttr  $t5, $zero, 1, 1, 0       # encoding: [0x60,0x20,0x70,0x0e]
  mttlo $t5, $ac0        # CHECK: mttr  $t5, $zero, 1, 1, 0       # encoding: [0x60,0x20,0x70,0x0e]
  mttlo $t5, $ac1        # CHECK: mttr  $t5, $4, 1, 1, 0         # encoding: [0x64,0x20,0x70,0x0e]
  mttlo $t5, $ac2        # CHECK: mttr  $t5, $8, 1, 1, 0         # encoding: [0x68,0x20,0x70,0x0e]
  mttlo $t5, $ac3        # CHECK: mttr  $t5, $12, 1, 1, 0         # encoding: [0x6c,0x20,0x70,0x0e]
  mtthi $t5              # CHECK: mttr  $t5, $1, 1, 1, 0         # encoding: [0x61,0x20,0x70,0x0e]
  mtthi $t5, $ac0        # CHECK: mttr  $t5, $1, 1, 1, 0         # encoding: [0x61,0x20,0x70,0x0e]
  mtthi $t5, $ac1        # CHECK: mttr  $t5, $5, 1, 1, 0         # encoding: [0x65,0x20,0x70,0x0e]
  mtthi $t5, $ac2        # CHECK: mttr  $t5, $9, 1, 1, 0         # encoding: [0x69,0x20,0x70,0x0e]
  mtthi $t5, $ac3        # CHECK: mttr  $t5, $13, 1, 1, 0         # encoding: [0x6d,0x20,0x70,0x0e]
  mttacx $t5             # CHECK: mttr  $t5, $2, 1, 1, 0         # encoding: [0x62,0x20,0x70,0x0e]
  mttacx $t5, $ac0       # CHECK: mttr  $t5, $2, 1, 1, 0         # encoding: [0x62,0x20,0x70,0x0e]
  mttacx $t5, $ac1       # CHECK: mttr  $t5, $6, 1, 1, 0         # encoding: [0x66,0x20,0x70,0x0e]
  mttacx $t5, $ac2       # CHECK: mttr  $t5, $10, 1, 1, 0         # encoding: [0x6a,0x20,0x70,0x0e]
  mttacx $t5, $ac3       # CHECK: mttr  $t5, $14, 1, 1, 0         # encoding: [0x6e,0x20,0x70,0x0e]
  mttdsp $a0             # CHECK: mttr  $a0, $16, 1, 1, 0         # encoding: [0x90,0x20,0x70,0x0e]
